// Helper to get all mutations for a species (for harvest screen)
export function getSpeciesMutations(speciesId) {
  return speciesMutations[speciesId] || [];
}

// Helper to find a mutation by ID across all species
export function findMutation(mutationId) {
  for (const species of Object.values(speciesMutations)) {
    const found = species.find(m => m.id === mutationId);
    if (found) return found;
  }
  return doctorMutations.find(m => m.id === mutationId) || null;
}
